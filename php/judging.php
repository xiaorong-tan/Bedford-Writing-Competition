				<p>“I was born in the house where my parents still live in Bedford. My sister was so pleased to have a sibling that she threw a thrupenny bit at me.</p>

				<p>As a child, I loved the Brownies but hated the Guides, was obsessed with ponies and read everything I could lay my hands on. Luckily, my mum worked in a bookshop. My favourite reads were The Moomintrolls, A Hundred Million Francs, The Lion, The Witch and The Wardrobe, the back of cereal packets, and gravestones.</p>

				<p>I passed enough O and A levels to get a place at Goldsmiths College, University of London, to study English and Drama. It was brilliant and I loved it.</p>

				<p>And then I got a proper job.</p>

				<p>I worked for ten years in a senior local government position (Human Resources – Recruitment, Diversity and Training). I was a square peg in round hole, but it paid the bills and mortgage.</p>

				<p>In my early thirties I had a car accident which left me unable to work full-time and convinced me to start writing seriously. I got a part-time job as an osteopath’s receptionist and spent all my spare time writing. It was all going well, but then in 2012 I got Cancer, which was bloody inconvenient but precipitated an exciting hair journey from bald to a peroxide blonde Annie Lennox crop. When chemo kept me up all night I passed the time writing, and the eventual result was THE KEEPER OF LOST THINGS. It was published by Two Roads in the UK in January 2017 and has recently become an Amazon Kindle No.1 bestseller. It was also published by Harper Collins in the US in February 2017, and will, this year be published in 13 further territories worldwide.</p>

				<p>On a personal note, I live in a chaotic Victorian house with an assortment of rescue dogs and my long-suffering husband. I spend all my free time writing or thinking about it and have notebooks in every room so that I can write down any ideas before I forget them. I am a magpie; always collecting treasures (or ‘junk’ depending on your point of view) and a huge John Betjeman fan. My favourite word is antimacassar and I still like reading gravestones.”</p>
